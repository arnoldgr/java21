//    Using the Queue and Deque Interfaces
//    You use a Queue when elements are added and removed in a specific order.
//    You can think of a queue as a line.
//    For example, when you want to enter a stadium and someone is waiting in line,
//    you get in line behind that person. And if you are British, you get in the queue behind that person,
//    making this really easy to remember! This is a FIFO (first-in, first-out) queue.
//    A Deque (double-ended queue), often pronounced “deck” extends Queue but is different
//    from a regular queue in that you can insert and remove elements from both the front (head) and back (tail).
//    Think, “Dr. Woodie Flowers, come right to the front!
//    You are the only one who gets this special treatment.
//    Everyone else will have to start at the back of the line.”

//    Comparing Deque Implementations
//    Reviewing Figure 9.1 one more time (you should know it well by now!),
//    LinkedList and ArrayDeque both implement the Deque interface, which inherits Queue.
//    You saw LinkedList earlier in the List section.
//    The main benefit of a LinkedList is that it implements both the List and Deque interfaces.
//    The trade-off is that it isn’t as efficient as a “pure” queue.
//    You can use the ArrayDeque class if you don’t need the List methods.
